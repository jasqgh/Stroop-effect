

( function($) {
    
    $(".pudelko").click(function() {
        $(this).css("display", "none");
        $('.red').each(function(){
            $(this).css({"left": Math.random() * window.innerWidth, "top": Math.random() * window.innerHeight}).css("display","block");
        
        });

       
    });


} ) ( jQuery );

